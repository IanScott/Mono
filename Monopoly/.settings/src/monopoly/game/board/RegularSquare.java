package monopoly.game.board;

/**
 * Represents a regular square. A regular square is a square that involves no
 * extra action from the player landing on it.
 * 
 * @author Open Universiteit
 */
public class RegularSquare extends Square {

  /**
   * Constructs a regular square.
   * 
   * @param name
   *          name of the square
   */
  public RegularSquare(String name) {
    super(name);
  }

}
